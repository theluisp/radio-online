					<h2>Política de privacidad</h2>

<p>Esta política de privacidad solo concierne a los usuarios de <em>Radio hispano </em>y a los usuarios de nuestros servicios en línea. Radio hispano trabaja para garantizar que su privacidad esté protegida al utilizar nuestros servicios. Por lo tanto, tenemos una política que establece cómo se procesarán y protegerán sus datos personales. Cualquier cambio que hayamos realizado en esta política de privacidad se puede encontrar visitándonos en línea.</p>

<h3>¿Cómo utilizamos sus datos personales?</h3>

<p>Al proporcionar sus datos personales, usted da su consentimiento para que utilicemos los datos recopilados para cumplir con nuestros compromisos con usted y brindarle el servicio que espera. Solo conservaremos sus datos durante el tiempo que sea necesario para prestarle nuestros servicios o durante el tiempo que nos exija la ley. Después de esto, sus datos personales serán eliminados. No podemos eliminar sus datos cuando existe un requisito legal de almacenamiento, como las reglas de contabilidad o cuando existen otras bases legales para conservar los datos, como una relación contractual en curso.</p>

<h3>Reglamento general de protección de datos (GDPR)</h3>

<p>En el Espacio Económico Europeo (EEE) solo le mostramos anuncios no personalizados. Estos anuncios no se basan en su comportamiento anterior y no utilizan cookies para la orientación de anuncios.</p>

<h3>¿Cuales son tus derechos?</h3>

<p>Tiene derecho a solicitar información sobre los datos personales que tenemos sobre usted. Si sus datos son incorrectos, incompletos o irrelevantes, puede solicitar que se corrija o elimine la información. Anualmente, tiene derecho a solicitar documentación escrita sobre la información personal que tenemos sobre usted en los archivos de nuestra cuenta. Para solicitar este documento, escriba al servicio de atención al cliente de Radio hispano. Puede retirar su consentimiento para que utilicemos los datos con fines de marketing (es decir, enviar catálogos, boletines u ofertas) en cualquier momento.</p>

<h3>¿Quién tiene acceso a los datos?</h3>

<p>Nunca cedemos, vendemos o intercambiamos sus datos a terceros con fines de marketing fuera del Grupo Radio hispano.</p>

<h3>¿Cómo protegemos sus datos?</h3>

<p>Hemos tomado medidas técnicas y organizativas para proteger sus datos de pérdida, manipulación, acceso no autorizado, etc. Adaptamos continuamente nuestras medidas de seguridad en línea con los avances y desarrollos tecnológicos. En Radio hispano protegemos sus datos mediante encriptación. Secure Sockets Layer (SSL) es una función que encripta toda la información enviada entre el comprador y el vendedor.</p>

<h3>Cookies</h3>

<p>Una cookie es un pequeño archivo de texto que se guarda y, durante visitas posteriores, se recupera de su computadora o dispositivo móvil. Radio hispano utiliza cookies para mejorar y simplificar su visita. No utilizamos cookies para almacenar información personal o para divulgar información a terceros. Existen dos tipos de cookies: permanentes y temporales (cookies de sesión). Las cookies permanentes se almacenan como un archivo en su computadora o dispositivo móvil por no más de 12 meses. Las cookies de sesión se almacenan temporalmente y desaparecen cuando cierra la sesión de su navegador. Utilizamos cookies permanentes para almacenar su elección de página de inicio y para almacenar sus datos si selecciona "Recordarme" cuando inicia sesión.</p>

<p>Puede borrar fácilmente las cookies de su computadora o dispositivo móvil usando su navegador. Para obtener instrucciones sobre cómo manejar y eliminar las cookies, consulte "Ayuda" en su navegador. Puede optar por desactivar las cookies o recibir una notificación cada vez que se envíe una nueva cookie a su computadora o dispositivo móvil. Tenga en cuenta que si elige deshabilitar las cookies, no podrá aprovechar todas nuestras funciones.</p>

<h3>Cookies de terceros</h3>

<p>Utilizamos cookies de terceros para recopilar estadísticas en forma agregada en herramientas de análisis como Facebook, Google AdSense, Google Analytics, Google+ y Twitter. Las cookies utilizadas son tanto permanentes como temporales (cookies de sesión). Las cookies permanentes se almacenan en su computadora o dispositivo móvil por no más de 24 meses.</p>

