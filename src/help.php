<h2>Ayuda</h2>

<p>Lamento escuchar que esta radio no tiene audio en su dispositivo. Pruebe las opciones a continuación para ver si el problema está relacionado con algo técnico en su dispositivo. Si esto no ayuda, envíenos un correo electrónico a noaudio@ekamsoftwares.com y mencione el nombre de la estación de radio y su país. ¡Si podemos, lo arreglamos de inmediato!</p>

<h3>Windows</h3>

<p>Necesita tener la última versión de <a href="http://windows.microsoft.com/en-in/windows/download-windows-media-player">Windows Media Player</a> instalada en su computadora. Puedes descargarlo <a href="http://windows.microsoft.com/en-in/windows/download-windows-media-player">aquí</a>. l</p>

<h3>Falta el complemento de Firefox</h3>

<p>Si Firefox le alerta sobre complementos faltantes, debe instalar el complemento <a href="http://www.interoperabilitybridges.com/Windows-Media-Player-Firefox-Plugin-Download">WMP Firefox</a>. Puedes descargarlo <a href="https://msopentechsitestorage.blob.core.windows.net/downloads/wmpfirefoxplugin.exe">aquí</a>.</p>

<p>Si este complemento de WMP Firefox no funciona correctamente en Firefox, busque <a href="https://support.mozilla.org/en-IN/questions/959680">aquí</a> la solución.</p>

<h3>Linux</h3>

<p>También puedes disfrutar de nosotros en Linux si estás usando Ubuntu, para hacerlo sigue los siguientes pasos:</p>

<ol>
	<li>Primero, abra el Administrador de paquetes.</li>
	<li>Luego, seleccione e instale los siguientes paquetes: gstreamer0.10-plugins-bad, gstreamer0.10-plugins-ugly, gstreamer0.10-plugins-ugly-multiverse, mplayer y gecko-mediaplayer.</li>
</ol>

<h3>Real player</h3>

<p>&nbsp;Además, es posible que necesite instalar <a href="http://www.real.com/realplayer/player-plus">Real Player</a> para ciertas estaciones de radio que solo transmiten con Real Audio. Puedes descargarlo <a href="http://www.real.com/realplayer/player-plus">aquí</a>.</p>
				
